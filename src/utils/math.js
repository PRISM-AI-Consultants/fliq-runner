// Math utilities for FLIQ Runner

export function lerp(a, b, t) {
  return a + (b - a) * t;
}

export function clamp(value, min, max) {
  return Math.max(min, Math.min(max, value));
}

export function randomRange(min, max) {
  return min + Math.random() * (max - min);
}

export function randomInt(min, max) {
  return Math.floor(randomRange(min, max + 1));
}

// Smooth easing for animations
export function easeOutCubic(t) {
  return 1 - Math.pow(1 - t, 3);
}

export function easeInOutQuad(t) {
  return t < 0.5 ? 2 * t * t : 1 - Math.pow(-2 * t + 2, 2) / 2;
}

export function easeOutElastic(t) {
  if (t === 0 || t === 1) return t;
  return Math.pow(2, -10 * t) * Math.sin((t * 10 - 0.75) * (2 * Math.PI / 3)) + 1;
}

// Smooth damping (frame-rate independent)
export function smoothDamp(current, target, speed, dt) {
  return lerp(current, target, 1 - Math.exp(-speed * dt));
}

// Generate a unique ID
let idCounter = 0;
export function uid() {
  return `id_${++idCounter}_${Date.now().toString(36)}`;
}
